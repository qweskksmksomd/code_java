package com.itheima.type;

public class TypeConversionDemo3 {
    public static void main(String[] args) {
        int a=20;
        byte b= (byte) a;
        System.out.println(b);
        System.out.println(a);
        int i=1500;
        byte j=(byte)i;
        System.out.println(j);
        double d=99.5;
        int m=(int)d;
        System.out.println(m);
    }
}
